
#include "darth_vader.h"

void beep(int note, int duration)
{
  //Play tone on buzzerPin
  tone(buzzerPin, note, duration);
  delay(duration);
  
  //Stop tone on buzzerPin
  noTone(buzzerPin);
  delay(50);
}

